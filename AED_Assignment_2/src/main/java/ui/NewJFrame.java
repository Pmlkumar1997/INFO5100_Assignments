    public void readCSV(){
        
        
        String line = "";
        String splitBy = ",";
        try {
            BufferedReader br = new BufferedReader(new FileReader(csvFilePath));  
        
            while ((line = br.readLine()) != null)    
            {  
            String[] carDetails = line.split(splitBy);
            //System.out.println(carDetails[7]);
            String Manufacturer = carDetails[0];
            int year = Integer.parseInt(carDetails[1]);
            int seats = Integer.parseInt(carDetails[2]);
            String model = carDetails[3];
            String avail = carDetails[4].strip();
            String maintenanceCertificate = carDetails[5].strip();
            String city = carDetails[6];
            int serialNumber = Integer.parseInt(carDetails[7]);
            
            
            Car car = new Car(Manufacturer,seats,model,serialNumber,year,city,avail,maintenanceCertificate);
            carlist.add(car);
            
             }  

            
        } catch (IOException e)   
            {  
            e.printStackTrace();  
            } 

    }

