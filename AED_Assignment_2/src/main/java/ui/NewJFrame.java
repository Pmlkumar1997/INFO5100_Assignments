    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String manufacturer = jTextField3.getText();
        String model = jTextField8.getText();
        String city = jComboBox3.getSelectedItem().toString();
        String strYear = jComboBox4.getSelectedItem().toString();
        String sNo = jTextField7.getText();
        String minSeats = jComboBox5.getSelectedItem().toString();
        String maxSeats = jComboBox6.getSelectedItem().toString();
        String maintenance = "";

        if (jRadioButton3.isSelected())
        {
            maintenance = "EXPIRED";
        }
        if (jRadioButton4.isSelected())
        {
            maintenance = "VALID";
        }

        if( manufacturer == "" && model == "" && city == "None" && strYear == "None" && sNo.equals("") && minSeats == "None" && maxSeats == "None"){

            JOptionPane.showMessageDialog(jButton3,"Please select options..!!","Search Car",2);
            return;

        }
        System.out.println();
        dtm.setRowCount(0);
        for(Car car: carlist)
        {

            if(!manufacturer.equals("") && !manufacturer.equals(car.Manufacturer))
            continue;

            if(!model.equals("") && !model.equals(car.model))
            continue;

            if(!city.equals("None") && !city.equals(car.city))
            continue;

            if(!strYear.equals("None") &&  !strYear.equals(String.valueOf(car.year)))
            continue;

            if(!sNo.equals("") && !sNo.equals(String.valueOf(car.serialNumber)))
            continue;

            if(!maintenance.equals("") && !maintenance.equals(car.maintenanceCertificate))
            continue;

            if(!minSeats.equals("None") && (car.seats < Integer.parseInt(minSeats)))
            continue;

            if(!maxSeats.equals("None") && (car.seats > Integer.parseInt(maxSeats)))
            continue;

            Object[] carObj = {car.Manufacturer,car.model,car.seats,
                car.city, car.year,car.serialNumber,car.avail,car.maintenanceCertificate };
            dtm.addRow(carObj);

        }

        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(dtm);
        jTable1.setRowSorter(tr);

    }//GEN-LAST:event_jButton3ActionPerformed

