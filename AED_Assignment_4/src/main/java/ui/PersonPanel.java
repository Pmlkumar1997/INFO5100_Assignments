    public  void displayPersons(List<Person> persons){
        model.setRowCount(0);
        for(Person person : persons){
            House house = person.getHouse();
            Community community = house.getCommunity();
            
            Object[] objs = {person.getFirstName(), person.getLastName(), person.getAge(), person.getSex(), house.getHno(),house.getStreet(),community.getCommunityName(),community.getCity().getCityName(),house.getState(),house.getZipcode(),person.getPhoneNumber(),person.getMaritalStatus()};
            model.addRow(objs);
        }
        
    }
