    private void btnSearchPersonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchPersonActionPerformed
        
        String fName = txtSearchFname.getText();
        String lName = txtSearchLname.getText();
        String community = boxSearchCommunity.getSelectedItem().toString();
        List<Person> searchedList= new ArrayList<>();
        
        if(fName.isEmpty() && lName.isEmpty() && community.equals("None")){
            JOptionPane.showMessageDialog(this, "All search fields are empty.!! Please check", "Empty Fields", 2);
            
        }
        else{
            
            for(Person person : personDirectory.getPersonDirectory()){
                House house = person.getHouse();
                if(community.equals("None") || community.equals(house.getCommunity())){
                    if((fName.isEmpty() || fName.equalsIgnoreCase(person.getFirstName())) && (lName.isEmpty() || lName.equalsIgnoreCase(person.getLastName())))
                        searchedList.add(person);
                }
            }
            
            
        }
        
        model.setRowCount(0);
        displayPersons(searchedList);
    }//GEN-LAST:event_btnSearchPersonActionPerformed
